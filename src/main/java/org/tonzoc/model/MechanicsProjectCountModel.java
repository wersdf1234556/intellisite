package org.tonzoc.model;

public class MechanicsProjectCountModel {

    private String dayCount;
    private String monthCount;
    private String yearCount;
    private String allCount;
    private String plusOilCount;

    public String getDayCount() {
        return dayCount;
    }

    public void setDayCount(String dayCount) {
        this.dayCount = dayCount;
    }

    public String getMonthCount() {
        return monthCount;
    }

    public void setMonthCount(String monthCount) {
        this.monthCount = monthCount;
    }

    public String getYearCount() {
        return yearCount;
    }

    public void setYearCount(String yearCount) {
        this.yearCount = yearCount;
    }

    public String getAllCount() {
        return allCount;
    }

    public void setAllCount(String allCount) {
        this.allCount = allCount;
    }

    public String getPlusOilCount() {
        return plusOilCount;
    }

    public void setPlusOilCount(String plusOilCount) {
        this.plusOilCount = plusOilCount;
    }
}
